package com.cg.l1.ui;

public class Cd {
	public static void main(String[] args) {
		
		int sum = 0;
		int sum1 = 0;
		int sum2 = 0;
		int sum3;
		int n = 2;
		for (int i = 1; i <= n; ++i) {
			sum = sum+i*i;
		}
		for (int x = 1; x <= n; ++x) {
			sum1 = sum1+x;
			sum2=sum1*sum1;
		}
		sum3=sum-sum2;  
		System.out.println("Sum = " + sum3);
		   
	}

}
